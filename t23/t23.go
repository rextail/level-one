func DelWithNewBase[S ~[]E, E any](arr S, index int) S {
	if index < 0 || index >= len(arr) {
		return arr
	}
	ans := make(S, 0, len(arr)-1)
	ans = append(ans, arr[:index]...)
	return append(ans, arr[index+1:]...)
}

func DelWithOldBase[S ~[]E, E any](arr S, index int) S {
	if index < 0 || index >= len(arr) {
		return arr
	}
	arr = append(arr[:index], arr[index+1:]...)
	return arr[:len(arr)]
}

func DisorderDel[S ~[]E, E any](arr S, index int) S {
	if index < 0 || index >= len(arr) {
		return arr
	}
	arr[index], arr[len(arr)-1] = arr[len(arr)-1], arr[index]
	return arr[:len(arr)-1]
}
